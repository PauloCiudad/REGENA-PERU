CREATE OR REPLACE FORCE EDITIONABLE VIEW "V_PROVEEDORES" ("IDCORRENTISTA", "CIUDAD_RES", "NOMBRE", "RAZON_COMERCIAL", "DOCUMENTO", "DIRECCION", "TELEFONO1", "TELEFONO2", "CORREO", "TIPO") AS 
  SELECT 
        A.IDCORRENTISTA,
        A.CIUDAD_RES,
        A.NOMBRE,
        A.RAZON_COMERCIAL,
        CASE
            WHEN RUC IS NULL THEN
                A.DNI
            ELSE
                A.RUC
        END DOCUMENTO,
        A.DIRECCION,
        A.TELEFONO1,
        A.TELEFONO2,
        A.CORREO,
        DECODE(B.TIPO,'P','PROVEEDOR') TIPO
    FROM 
        CORRENTISTAS A,
        correntista_tipo B
    WHERE
        A.IDCORRENTISTA=B.IDCORRENTISTA AND
        B.TIPO = 'P' AND
        A.ESTADO=0;