CREATE TABLE CORRENTISTA_TIPO (
    IDCORRENTISTA_TIPO    NUMBER PRIMARY KEY,
    IDCORRENTISTA         NUMBER NOT NULL,
    TIPO                  VARCHAR2(1) NOT NULL,
    fecha_creacion        DATE DEFAULT SYSDATE NOT NULL,
    usuario_creacion      VARCHAR2(100) DEFAULT USER NOT NULL,
    fecha_modificacion    DATE,
    usuario_modificacion  VARCHAR2(100), 
    ESTADO NUMBER(1) DEFAULT 0 NOT NULL,

    CONSTRAINT FK_CT_CORRENTISTA FOREIGN KEY (IDCORRENTISTA)
        REFERENCES CORRENTISTAS(IDCORRENTISTA),
    CONSTRAINT CK_CT_TIPO CHECK (TIPO IN ('C','P','T')),
    CONSTRAINT UK_CT_CORR_TIPO UNIQUE (IDCORRENTISTA, TIPO)
);

CREATE OR REPLACE FORCE EDITIONABLE VIEW V_CLIENTES AS 
  SELECT 
        A.IDCORRENTISTA,
        A.CIUDAD_RES,
        A.NOMBRE,
        A.RAZON_COMERCIAL,
        CASE
            WHEN RUC IS NULL THEN
                A.DNI
            ELSE
                A.RUC
        END DOCUMENTO,
        A.DIRECCION,
        A.TELEFONO1,
        A.TELEFONO2,
        A.CORREO,
        DECODE(B.TIPO,'C','CLIENTE') TIPO
    FROM 
        CORRENTISTAS A,
        correntista_tipo B
    WHERE
        A.IDCORRENTISTA=B.IDCORRENTISTA AND
        B.TIPO = 'C' AND
        A.ESTADO=0;

