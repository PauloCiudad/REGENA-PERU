CREATE TABLE LUGARES (
    IDLUGAR NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LUGAR_SUP NUMBER,
    NOMBRE VARCHAR2(200),
    ABREVIACION VARCHAR2(30),
    fecha_creacion        DATE DEFAULT SYSDATE NOT NULL,
    usuario_creacion      VARCHAR2(100) DEFAULT USER NOT NULL,
    fecha_modificacion    DATE,
    usuario_modificacion  VARCHAR2(100),
    ESTADO NUMBER(1) DEFAULT 0 NOT NULL,
    CONSTRAINT FK_LUGARES_IDLUGAR FOREIGN KEY (LUGAR_SUP)
        REFERENCES LUGARES(IDLUGAR)
);

CREATE TABLE CORRENTISTAS (
    IDCORRENTISTA NUMBER PRIMARY KEY,
    SEXO VARCHAR2(1),
    CIUDAD_RES NUMBER(10),
    NOMBRE VARCHAR(200) NOT NULL,
    RAZON_COMERCIAL VARCHAR2(200),
    ruc                   NUMBER(11),
    DNI     NUMBER(8),
    direccion             VARCHAR2(300),
    telefono1              VARCHAR2(50),
    telefono2              VARCHAR2(50),
    correo                VARCHAR2(150),
    fecha_creacion        DATE DEFAULT SYSDATE NOT NULL,
    usuario_creacion      VARCHAR2(100) DEFAULT USER NOT NULL,
    fecha_modificacion    DATE,
    usuario_modificacion  VARCHAR2(100),
    ESTADO NUMBER(1) DEFAULT 0 NOT NULL,

    CONSTRAINT FK_CORR_CIUDRES FOREIGN KEY (CIUDAD_RES)
        REFERENCES LUGARES(IDLUGAR),
    CONSTRAINT uk_CLI_RUC UNIQUE (ruc),
    CONSTRAINT uk_CLI_DNI UNIQUE (DNI)
);

CREATE OR REPLACE TRIGGER CORRENTISTAS_BU
BEFORE UPDATE ON CORRENTISTAS
REFERENCING OLD AS OLD NEW AS NEW
FOR EACH ROW
BEGIN
    :NEW.usuario_modificacion := nvl(v('APP_USER'),USER);
    :NEW.fecha_modificacion := SYSDATE;
END;

